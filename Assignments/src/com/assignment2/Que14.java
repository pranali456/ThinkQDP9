package com.assignment2;

public class Que14 {

	public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int c;
        int d;

        c = ++b;
        d = a++;

        c++;

        System.out.println("a = " + a);
        System.out.print("b = " + b);
        System.out.println("c = " + c);
        System.out.print("d = " + d);


	}

}
